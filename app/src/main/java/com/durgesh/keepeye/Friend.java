package com.durgesh.keepeye;

public class Friend {
    private String id;
    private String name;

    public Friend(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }
    public String setName() { return  name; }

    public String getName() {
        return name;
    }
}
